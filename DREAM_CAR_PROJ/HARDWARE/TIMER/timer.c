#include "timer.h"
#include "led.h"

u8 timeout_triple=0;//定时器溢出次数


//通用定时器2中断初始化
//arr：自动重装值。
//psc：时钟预分频数
//定时器溢出时间计算方法:Tout=((arr+1)*(psc+1))/Ft us.
//Ft=定时器工作频率,单位:Mhz
//这里使用的是定时器2!
void TIM6_Init(u16 arr,u16 psc)
{
	TIM_TimeBaseInitTypeDef TIM_TimeBaseInitStructure;
	NVIC_InitTypeDef NVIC_InitStructure;
	
	RCC_APB1PeriphClockCmd(RCC_APB1Periph_TIM6,ENABLE);  ///使能TIM6时钟
	
  TIM_TimeBaseInitStructure.TIM_Period = arr; 	//自动重装载值
	TIM_TimeBaseInitStructure.TIM_Prescaler=psc;  //定时器分频
	TIM_TimeBaseInitStructure.TIM_CounterMode=TIM_CounterMode_Up; //向上计数模式
	TIM_TimeBaseInitStructure.TIM_ClockDivision=TIM_CKD_DIV1; 
	
	TIM_TimeBaseInit(ENCODER_OMEGA_TIM,&TIM_TimeBaseInitStructure);
	
	TIM_ITConfig(ENCODER_OMEGA_TIM,TIM_IT_Update,ENABLE); //允许定时器6更新中断
	TIM_Cmd(ENCODER_OMEGA_TIM,ENABLE); //使能定时器2
	
	NVIC_InitStructure.NVIC_IRQChannel = ENCODER_OMEGA_IRQn; //定时器6中断
	NVIC_InitStructure.NVIC_IRQChannelPreemptionPriority = ENCODER_OMEGA_TIM_PreemptionPriority; //抢占优先级1
	NVIC_InitStructure.NVIC_IRQChannelSubPriority = ENCODER_OMEGA_TIM_SubPriority; //子优先级3
	NVIC_InitStructure.NVIC_IRQChannelCmd=ENABLE;
	NVIC_Init(&NVIC_InitStructure);
	
}

//定时器6中断服务函数
void ENCODER_OMEGA_IRQHandler(void)
{
	if(TIM_GetITStatus(ENCODER_OMEGA_TIM,TIM_IT_Update)==SET) //溢出中断
	{
		//LED1=!LED1;
			timeout_triple++;
	}
	TIM_ClearITPendingBit(ENCODER_OMEGA_TIM,TIM_IT_Update);  //清除中断标志位
}
